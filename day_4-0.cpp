/*
********** OOPS **********
The building block of C++ that leads to Object-Oriented 
programming is a Class. It is a user-defined data type,
which holds its own data members and member functions,
which can be accessed and used by creating an 
instance of that class.A class is like a blueprint for an object

An Object is an identifiable entity with some characteristics and behaviour. 
An Object is an instance of a Class. When a class is defined, 
no memory is allocated but when it is 
instantiated (i.e. an object is created) memory is allocated.

In Object-Oriented Programming, Encapsulation is defined 
as binding together the data and the functions that manipulate them.

Data abstraction refers to providing only essential information 
about the data to the outside world, hiding the 
background details or implementation.

we can define polymorphism as the ability of 
a message to be displayed in more than one form.

The capability of a class to derive properties and 
characteristics from another class is called Inheritance

In dynamic binding, the code to be executed in response to 
function call is decided at runtime.

Message Passing: Objects communicate with one another by 
sending and receiving information to each other.
*/