// Polymorphism in C++

// “Poly” means several and “morphism” means form. So we can say that polymorphism is something that has several forms
//  or we can say it as one name and multiple forms. There are two types of polymorphism:

// Compile Time Polymorphism

// In compile-time polymorphism, it is already known which function will run. Compile-time polymorphism is also called 
// early binding, which means that you are already bound to the function call and you know that this function is going 
// to run. This type of polymorphism is achieved by function overloading or operator overloading.

// Run Time Polymorphism

// In the run-time polymorphism, the compiler doesn’t know already what will happen at run time. Run time polymorphism 
// is also called late binding. The run time polymorphism is considered slow because function calls are decided at 
// run time. Run time polymorphism can be achieved from the virtual function.
